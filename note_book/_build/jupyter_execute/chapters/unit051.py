# # Introduction to Factor Markets

# ```{admonition} Recap
# Factors of Production
# - Land (paid by **Rent**)
# - Labor (paid by **Wage**)
# - Capital (paid by **Interest**)
#     - Physical capital<br>Any human-made resource that is used to create other goods and services.
#     - Human capital<br>Any skills or knowledge gained by a worker through education and experience.
# - Entrepreneurship (paid by **Profit**)
# <br><br>
# In the product market, individuals (consumers) pay businesses for goods and services.<br>
# In the factor market, businesses (consumers) pay individuals for the use of resources. 
# ```

# ```{admonition} Definitions
# Derived demand
# : Demand for the factors of production because this demand is derived from goods that are produced by these inputs.<br><br>
# 
# Monopsony
# : Occurs when there is a single buyer of labor.
# : Monopoly of the factor market.
# : Similar to monopolies, a monopsony underhires and pays workers less than in a competitive market.
# ```

