# ```{important}
# **Profit-Maximizing rule** for employing resources:<br>
# MRP = MFC
# 
# 
# A firm maximizes its profits by continuing to hire inputs used in production if the MRP > MFC up until the profit-maximizing point where MRP = MFC.<br><br>
# 
