#   </mfrac>
# </math>
# <br>
# 
# Marginal cost/MC
# : Additional cost incurred by an additional output.
# : <math xmlns="http://www.w3.org/1998/Math/MathML">
#   <mi>M</mi>
#   <mi>C</mi>
#   <mo>=</mo>
