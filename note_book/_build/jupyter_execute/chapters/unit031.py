# 
# Fixed cost/FC:
# : Cost that does not depend on the quantity of output produced. It is the cost of
# the fixed input.
# : e.g. rent, machines.<br><br>
# 
# Variable cost/VC
# : Cost that depends on the quantity of output produced.
# : e.g. Cost of labor, which is equal to the number of workers multiplied by weekly pay.<br><br>
# 
# Total cost/TC
# : TC = FC + VC<br><br>
# 
# Average variable cost/AVC
