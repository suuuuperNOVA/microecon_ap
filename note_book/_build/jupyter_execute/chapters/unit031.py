# 
# Average product/AP
# : Output per unit of input.
# : <math xmlns="http://www.w3.org/1998/Math/MathML">
#   <mi>A</mi>
#   <mi>P</mi>
#   <mo>=</mo>
#   <mfrac>
#     <mtext>Total Product</mtext>
